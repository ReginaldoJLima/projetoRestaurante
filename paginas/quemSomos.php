    <h5>Proteção dos alimentos</h5>
    <br>
    <div class="container-fluid"></div><img src="public/img/corona4.jpg" alt="" class="img-fluid rounded-circle">
    <hr>

    <p>Se você trabalha em um negócio em que os alimentos geralmente ficam expostos – como buffet ou padaria, por exemplo –, deve ficar atento a essa questão. Para evitar que os produtos sejam contaminados, é aconselhável protegê-los.

      Para isso, você pode utilizar embalagens individuais, tampas ou mesmo um pano ou papel mais extenso para cobrir os itens.

      Em alguns casos, é aconselhável deixar um funcionário responsável por manusear os alimentos e entregar aos clientes. Em uma padaria, por exemplo, ao invés de expor os pães em uma cesta, você pode deixá-los na área interna do balcão, onde o cliente deverá solicitar o item que deseja.</p>
    <hr>
    <h5>Número de mesas no estabelecimento reduzidas</h5>
    <br>
    <div class="container-fluid"></div><img src="public/img/outback-eldorado-06.jpg" alt="" class="img-fluid rounded-circle">
    <br>
    <p>Sabemos que, como empresário, você quer ver seu estabelecimento sempre lotado. Contudo, nessa crise pela qual estamos passando, é importante tomar medidas para evitar o acúmulo de muitas pessoas em um mesmo ambiente.

      Isso porque a transmissão da COVID-19 acontece da mesma maneira como a do vírus da gripe comum – ou seja, por meio das gotículas de saliva que saltam quando espirramos ou tossimos ou que são repassadas pelas mãos e por objetos compartilhados.

      É por esse motivo que uma das recomendações dos especialistas para dificultar a propagação do coronavírus é evitar lugares com aglomerações de pessoas.

      Então, você pode fazer a sua parte no seu negócio, limitando o número de mesas e aumentando o espaço entre elas, para evitar contato próximo.

      “Boas práticas certamente darão uma segurança enorme a nossos clientes. A mais importante é a redução do número de mesas, para assegurar que a distância entre cadeiras ocupadas seja de, no mínimo, um metro; ou de, pelo menos, dois metros entre as mesas, sem considerar as cadeiras”, destaca Paulo Solmucci, presidente da Abrasel.</p>
    <hr>
    <h5>Cuidado com as Operações de Delivery</h5>
    <br>
    <div class="container-fluid"></div><img src="public/img/chopp.jpg" alt="" class="img-fluid rounded-circle">
    <p>Quem trabalha com entrega de comida pode até sentir um aumento no movimento nesse contexto em que as pessoas estão com medo de comer fora de casa. Contudo, isso demanda um senso de responsabilidade ainda maior com os clientes que estão confiando em suas operações nesse momento.

      Da mesma forma que é preciso tomar medidas preventivas em bares, restaurantes e lanchonetes – como as ações que indicamos acima –, também é crucial que os negócios que trabalham com delivery tomem os cuidados necessários.

      Por isso, se você faz entrega de alimentos, precisa:

      – Reforçar a limpeza do ambiente onde os alimentos são manipulados;
      – Atentar-se à higiene das pessoas e de utensílios, equipamentos e superfícies que entram em contato com o alimento;
      – Verificar se os entregadores estão tomando as devidas medidas preventivas;
      – Reforçar o cuidado com as embalagens. Por exemplo: colocar uma camada protetora extra para que as mãos do entregador e a superfície do transporte não entrem em contato com a embalagem do alimento.</p>
    <hr>
    <h5>3 ações que negócios de food service podem realizar para atender às necessidades dos consumidores em relação à prevenção da COVID-19</h5>
    <br>
    <div class="container-fluid"></div><img src="public/img/matesclub.jpg" alt="" class="img-fluid rounded">
    <hr>
    <p> 1. Algumas das ações as pessoas poderão presenciar no próprio estabelecimento, quando elas virem os funcionários mais protegidos, as operações adaptadas e as superfícies sendo constantemente limpas. No entanto, também é importante se posicionar oficialmente.

      Em comunicados via flyers, cartazes, redes sociais e site, liste as precauções que o seu negócio está tomando em relação à COVID-19. Isso vai dar mais confiança para as pessoas frequentarem seu estabelecimento.</p>
    <br>
    <p> 2. Algumas pessoas são mais vulneráveis ao novo coronavírus. Quem tem mais de 60 anos, é hipertenso, diabético ou possui problemas respiratório e doenças autoimunes está entre o chamado grupo de risco. Uma vez infectadas, essas pessoas têm muito mais chance de desenvolverem complicações sérias.

      É recomendado às pessoas que fazem parte desse grupo ficarem isoladas. Para aliviar o distanciamento desse público, no exterior alguns restaurantes estão oferecendo um horário para atendimento exclusivo de idosos. Assim, eles podem comer fora sem o risco de se exporem a alguém infectado (e que ainda não tenha apresentado os sintomas).

      Que tal adotar essa ideia em seu estabelecimento?</p>
    <br>
    <p> 3. Se o seu estabelecimento não oferece serviço de entrega, considere oferecer nesse momento. Nem que seja para os seus clientes mais fiéis.

      Digamos que funcionários de determinada empresa sempre almocem no seu restaurante. Para continuar atendendo-os neste período de crise, ofereça entrega de marmitas.

      Além disso, outra maneira de evitar aglomerações em seu estabelecimento, mas continuar atendendo aos clientes, é oferecendo outras maneiras de servi-los. Por exemplo: possibilite que os clientes façam o pedido via WhatsApp ou por telefone e retirem no seu restaurante.

      Sabemos que esse é um momento difícil para todos, e que com certeza vai afetar as operações de negócios de todos os tamanhos. O importante é fazer sua parte e, na medida do possível, continuar atendendo da forma mais segura possível os clientes que estão contando com o seu serviço no dia a dia.

      Além disso, pode ter certeza de que o Roldão, seu parceirão de bons negócios, está à sua disposição para ajudá-lo a enfrentar essa turbulência</p>
